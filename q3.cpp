//Q3. How much space does the following data types take?
//int float bool
#include<iostream>
using namespace std;
int main(){
    int a;
    cout<<"Size of int = "<<sizeof(a)<<endl;
    float b;
    cout<<"Size of float = "<<sizeof(b)<<endl;
    bool flag;
    cout<<"size of bool = "<<sizeof(flag);
    return 0;
}